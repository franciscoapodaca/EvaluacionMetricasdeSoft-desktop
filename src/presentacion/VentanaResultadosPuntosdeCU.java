    public void calcularTotalFactoresTecnicos() {
        tf = Double.parseDouble(txtPesoTF.getText());
        double resultadoTF1 = (0.01) * tf;
        resultadoTF = resultadoTF1 + 0.6;
        String fresult = df.format(resultadoTF);
        txtCalculoTF.setText(fresult);
    }

    public void calcularTotalFactoresAmbientales() {
        ef = Double.parseDouble(txtPesoEF.getText());
        double resultadoEF1 = (-0.03) * ef;
        resultadoEF = resultadoEF1 + 1.4;
        txtCalculoEF.setText(new Double(resultadoEF).toString());
    }

    public void calcularPuntosdeCU() {
        aw = Double.parseDouble(txtPesoAW.getText());
        ucw = Double.parseDouble(txtPesoUCW.getText());
        double resultado = (aw + ucw) * resultadoTF * resultadoEF;
        String xresultado = df.format(resultado);
        txtCalculoPuntosCU.setText(xresultado);

    }

    public void calcularEsfuerzo() {
        double CU = Double.parseDouble(txtCalculoPuntosCU.getText());
        double esfuerzo = CU * 20;
        txtCalculoEsfuerzo.setText(new Double(esfuerzo).toString());
    }

