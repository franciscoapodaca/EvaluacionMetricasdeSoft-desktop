    private void comboBoxLineasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxLineasActionPerformed
        if (comboBoxLineas.getSelectedItem().equals("Ensamblador")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 320;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("C")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 128;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("C++")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 29;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Fortan")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 105;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Pascal")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 91;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Java")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 30;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Generadores de código")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 15;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Spreadsheet")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 6;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }
        if (comboBoxLineas.getSelectedItem().equals("Lenguajes gráficos")) {
            double psa = Double.parseDouble(txtPuntosSinAjustar.getText());
            resultado = psa * 4;
            txtLineasdeCodigo.setText(new Double(resultado).toString());
        }

    }//GEN-LAST:event_comboBoxLineasActionPerformed

