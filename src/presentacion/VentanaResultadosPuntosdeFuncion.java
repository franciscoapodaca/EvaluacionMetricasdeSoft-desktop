    public void calcularFactordeAjuste() {
        gi = Double.parseDouble(txtGI.getText());
        resultadoGI = (gi * 0.01) + 0.65;
        String fresult = df.format(resultadoGI);
        txtFactordeAjuste.setText(fresult);
    }

    public void calcularPuntosdeFuncion() {
        pf = Double.parseDouble(txtPuntosSinAjustar.getText());
        double resultado = (pf * resultadoGI);
        String fresult = df.format(resultado);
        txtPuntosdeFuncion.setText(fresult);
    }

